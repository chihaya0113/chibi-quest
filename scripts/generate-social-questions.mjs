function repeatUnit(unit, base, total, tags) {
  const hardIndexes = {
    local_city: new Set([3, 4, 6, 7, 8, 9]),
    local_work: new Set([2, 4, 6, 7, 8]),
    safety_life: new Set([4, 5, 6, 7, 8, 9]),
    city_change: new Set([1, 2, 3, 5, 6, 7, 9])
  };
  const contexts = [
    "",
    "まち探検のまとめで考えよう。 ",
    "友だちに説明するとしたら。 ",
    "資料カードを見たつもりで考えよう。 ",
    "家の人に話すなら。 ",
    "発表メモに書くなら。 ",
    "見学のあとに考えよう。 ",
    "ノートにまとめるなら。 "
  ];
  const questions = [];
  let index = 1;
  while (questions.length < total) {
    const baseIndex = questions.length % base.length;
    const [prompt, correct, wrongs, explanation, difficulty] = base[baseIndex];
    const finalDifficulty = hardIndexes[unit]?.has(baseIndex) ? 3 : difficulty;
    const round = Math.floor(questions.length / base.length);
    const context = contexts[round % contexts.length];
    questions.push(q(unit, index++, finalDifficulty, `${context}${prompt}`, correct, wrongs, explanation, tags, `${unit}_${baseIndex}`));
  }
  return questions;
}

const questions = [
  ...makeLocalCity(),
  ...makeLocalWork(),
  ...makeSafetyLife(),
  ...makeCityChange()
];

if (questions.length !== 260) {
  throw new Error(`Expected 260 social questions, got ${questions.length}`);
}

for (const question of questions) {
  const texts = question.choices.map((choice) => choice.text);
  if (texts.length !== 4 || new Set(texts).size !== 4 || !texts.includes(question.answer.value)) {
    throw new Error(`Invalid choices: ${question.id}`);
  }
}

await mkdir(new URL("../src/data/questions/grade3/social/", import.meta.url), { recursive: true });
writeFileSync(outFile, `window.CHIBI_QUEST_SOCIAL_QUESTIONS = ${JSON.stringify(questions, null, 2)};\n`);
console.log(`Generated ${questions.length} social questions`);
