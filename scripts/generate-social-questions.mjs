function pad(number) {
  return String(number).padStart(3, "0");
}

function choices(correct, wrongs) {
  return [correct, ...wrongs].map(String).slice(0, 4).map((text, index) => ({
    id: String.fromCharCode(97 + index),
    text
  }));
}

function q(unit, index, difficulty, prompt, correct, wrongs, explanation, tags, familyId) {
  return {
    id: `g3_social_${unit}_${pad(index)}`,
    version: 1,
    grade: 3,
    subject: "social",
    unit,
    unitLabel: unitLabels[unit],
    curriculumArea: unitAreas[unit],
    difficulty,
    questionType: "multiple_choice",
    prompt,
    choices: choices(correct, wrongs),
    answer: {
      type: "choice",
      value: String(correct)
    },
    explanation,
    estimatedSeconds: 45,
    skillTags: tags,
    familyId,
    sourcePolicy: {
      basis: ["学習指導要領", "教科書目次"],
      usesTextbookText: false,
      originalContent: true
    },
    status: "active"
  };
}

