function pad(number) {
  return String(number).padStart(3, "0");
}

function choices(correct, wrongs) {
  const fallback = ["ア", "イ", "ウ", "エ", "そのほか"];
  const values = [...new Set([correct, ...wrongs].map(String))];
  for (const item of fallback) {
    if (values.length >= 4) break;
    if (!values.includes(item)) values.push(item);
  }
  return values.slice(0, 4).map((text, index) => ({
    id: String.fromCharCode(97 + index),
    text
  }));
}

function q(unit, index, difficulty, prompt, correct, wrongs, explanation, extra = {}) {
  const adjustedDifficulty = Math.max(2, difficulty);
  return {
    id: `g3_ja_${unit}_${pad(index)}`,
    version: 1,
    grade: 3,
    subject: "japanese",
    unit,
    unitLabel: unitLabels[unit],
    curriculumArea: extra.curriculumArea ?? "知識及び技能",
    difficulty: adjustedDifficulty,
    questionType: "multiple_choice",
    prompt,
    choices: choices(correct, wrongs),
    answer: {
      type: "choice",
      value: String(correct)
    },
    explanation,
    estimatedSeconds: extra.estimatedSeconds ?? 45,
    skillTags: extra.skillTags ?? [unitLabels[unit]],
    sourcePolicy: {
      basis: ["学習指導要領", "教科書目次"],
      usesTextbookText: false,
      originalContent: true
    },
    status: "active"
  };
}

